# Creating an array (list)
my_array = [1, 2, 3, 4, 5]

# Accessing elements
print(my_array[0])  # Output: 1

# Modifying elements
my_array[2] = 10
print(my_array)  # Output: [1, 2, 10, 4, 5]

# Adding elements
my_array.append(6)
print(my_array)  # Output: [1, 2, 10, 4, 5, 6]

# Removing elements
my_array.remove(4)
print(my_array)  # Output: [1, 2, 10, 5, 6]
